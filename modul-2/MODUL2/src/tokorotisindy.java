    private void TotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotalActionPerformed
        // TODO add your handling code here:
        int hargaRoti = 0;
        int hargaTopping = 0;
     
        // Cek menu roti yang dipilih dan harganya
        if (DanishCoklat.isSelected()) {
            hargaRoti = 200000;
        } else if (LapisLegit.isSelected()) {
            hargaRoti = 60000;
        } else if (LemonCake.isSelected()) {
            hargaRoti = 50000;
        } else if (BrowniesKeju.isSelected()) {
            hargaRoti = 40000;
        } else if (RotiCroffle.isSelected()) {
            hargaRoti = 30000;
        } else if (Muffin.isSelected()) {
            hargaRoti = 40000;
        } else if (BrowniesCoklat.isSelected()) {
            hargaRoti = 40000;
        } else if (PiaLumer.isSelected()) {
            hargaRoti = 30000;
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Salah Satu Menu Roti!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Cek topping yang dipilih dan harganya
        if (Oreo.isSelected()) {
            hargaTopping = 3000;
        } else if (CocoCips.isSelected()) {
            hargaTopping = 4000;
        } else if (Kacang.isSelected()) {
            hargaTopping = 3000;
        } else if (KacangAlmond.isSelected()) {
            hargaTopping = 6000;
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Salah Satu Topping!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            return;
        }
                
        int total1 = (hargaRoti + hargaTopping) * jumlahPesanan;

        // Diskon jika total lebih dari Rp. 100.000
        if (total1 > 100000) {
            total1 -= total1 * 5 / 100;
        } HasilTotal.setText("Rp. " + total1);
                
        String cashText = InputCash.getText();
        // Periksa apakah teks kosong
            if (cashText.isEmpty()) { 
                HasilUangKembalian.setText("");
            return; 
            }
                
        int cash = Integer.parseInt(cashText); // Konverensi ke int
        int kembalian = cash - total1; 
        // Jika uang cash tidak cukup
            if (kembalian < 0) { 
                HasilUangKembalian.setText("");
                JOptionPane.showMessageDialog(this, "Uang cash tidak cukup!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            } else {
                HasilUangKembalian.setText("Rp. " + kembalian);
            }
    }//GEN-LAST:event_TotalActionPerformed

